package graph.simple;

import graph.standard.StandardGraph;
import graph.standard.Link;

/**
 * A simple graph modelling using integers as nodes
 * @author CreeperStone72
 */
public class SimpleGraph extends StandardGraph<Integer> {
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors ///////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * Empty constructor. Creates an oriented graph
     */
    public SimpleGraph() { super(); }

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Methods ////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    /**
     * Creates the adjacency matrix from the graph
     * @return a matrix that models all nodes and links
     */
    public Matrix getAdjacencyMatrix() {
        Matrix m = new Matrix(order());

        for(Link link : getLinks())
            m.setValue((int) link.getX().getData(), (int) link.getY().getData(), 1);

        return m;
    }
}
